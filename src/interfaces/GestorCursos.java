    public void agregarCurso(Curso curso) {

        cursos.add(curso);
        listaCursos.addElement(curso.getTexto());
        lstCursos.setModel(listaCursos);
    }

    public void editarCurso(Curso curso, int index) {

        Curso cursoAnterior = cursos.get(index);
        cursoAnterior.setNombre(curso.getNombre());
        cursoAnterior.setCreditos(curso.getCreditos());
        cursoAnterior.setCodigo(curso.getCodigo());
        actualizarLista();
    }

    public void actualizarLista() {

        listaCursos.removeAllElements();
        for (Curso curso : this.cursos) {
            listaCursos.addElement(curso.getTexto());
        }

        lstCursos.setModel(listaCursos);

    }

